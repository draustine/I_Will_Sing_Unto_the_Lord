package com.precise.i_will_sing_unto_the_lord;

public class Hymn {

    private String title;
    private String text;

    public Hymn(String title, String text) {
        this.title = title;
        this.text = text;
    }

    public String getTitle() {
        return title;
    }

    public String getText() {
        return text;
    }
}
